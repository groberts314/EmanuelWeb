        <h2>Traditional Service (9:00 AM)</h2>
        <p>
          Join us on Sunday mornings at 9:00 for our Traditional Worship service, a traditional Lutheran liturgical service with prayers
          and litany  from our hymnal Evangelical Lutheran Worship. Services features hymns, old and new, and are supported by an organ,
          the Chancel Choir and Handbell Choir.  We celebrate Holy Communion every Sunday. A children’s sermon is a regular part of this service.
          Our Traditional worship includes a sermon by our pastor, which is based on the scripture readings for the day.
        </p>
        <h2>Praise Service (10:45 AM)</h2>
        <p>
          This worship celebration features an informal style and contemporary music performed by the &ldquo;Praise Band.&rdquo;
          We celebrate the Lord&rsquo;s Supper every Sunday and gather around the altar to celebrate together. A children&rsquo;s
          sermon is a regular part of this service and children are regularly invited up to join the band in playing a tamborine
          or other rhythm instrument. Our Praise worship includes a sermon by our pastor, which is based on the scripture
          readings for the day.
        </p>
        <h2>Frequently Asked Questions</h2>
        <h3>What about my children?</h3>
        <p>
          Children are important to the life of Emanuel Lutheran Church and children are always welcome to stay with you
          during the worship service. The typical noise and movement of children reminds us that we are responsible for
          passing on our faith to our young people. There is always a children&rsquo;s cart where coloring pages, books,
          crayons and the weekly children&rsquo;s bulletin are available.  However, if you prefer, a &ldquo;cry room&rdquo;
          is also available at the rear of the sanctuary with rocking chairs and a few children&rsquo;s toys. The parent
          is still able to see and hear the service from the cry room area.
        </p>
        <h3>Hearing Issues?</h3>
        <p>
          Hearing assistance devices are available on the small table in the west narthex. Simply return them when you have
          finished and remember to turn them off.
        </p>
        <h3>Should I receive Holy Communion?</h3>
        <p>
          All are welcome to receive the sacrament of the Lord’s Supper at Emanuel Lutheran. The ushers will indicate when it
          is time for you to approach the front. If you choose not to receive Communion, please feel free to come forward for
          a blessing.
        </p>
        <h3>What should I wear to church?</h3>
        <p>
          There is no right way to dress at Emanuel Lutheran Church. Some people get quite dressed up and others dress more
          casually. Wear whatever makes you comfortable.
        </p>
        <h3>Do you have Sunday School?</h3>
        <p>
          Education Hour is provided for age three through teens at 10:00 a.m. Children gather in Moerer Hall before moving
          to their classroom in the Education building. Teens meet in the Youth room at the Southern end of the campus.   
        </p>
        <h3>Is there a social time after the service?</h3>
        <p>
          Please join us after the service for a cup of coffee in the Fellowship Hall. This is a great time to meet new friends.
        </p>
